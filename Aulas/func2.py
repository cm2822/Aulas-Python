def soma(a=0, b=0, c=0):
    """
    -> Esta função imprime no ecrã a soma de variáveis
    :param a: primeiro número da soma
    :param b: segundo número da soma
    :param c: terceiro número da soma
    :return: retorna a soma  das varáiveis
    """
    s= a + b + c
    return s


resultado = soma(1, 2, 3)
resultado2 = soma(75, 45, 52)
print(resultado)
print(resultado2)
